
public class Ejercicio9 {

	public static void main(String[] args) {

		
		//creamos un bucle del 1 al 100
		for (int i = 1; i <= 100; i++) {
			//condicion si el numero es divisible entre 2 imprimelo y dice que es divisible entre 2
			if (i % 2 == 0) {
				System.out.println(i + " - Divisible entre 2");
			}
			//condicion si el numero es divisible entre 3 imprimelo y dice que es divisible entre 3
			if (i % 3 == 0) {
				System.out.println(i + " - Divisible entre 3");

			}
			
		}
		
	}

}
